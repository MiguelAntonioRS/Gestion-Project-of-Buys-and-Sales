    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        MCategory dts = new MCategory();
        NCategory func = new NCategory();
        
        if (txtCategory.getText().length()==0) {
            
            JOptionPane.showMessageDialog(null, "Ingrese la Categoria");
            txtCategory.requestFocus();
            return;
        }
        dts.setnCategory(txtCategory.getText());
        
        if (func.insert(dts)) {
            
            JOptionPane.showMessageDialog(null, "Categoria Registrada");
            txtCategory.setText("");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

